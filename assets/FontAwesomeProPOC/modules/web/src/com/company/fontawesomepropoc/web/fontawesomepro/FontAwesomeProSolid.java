    public static FontAwesomeProSolid fromCodepoint(final int codepoint) {
        for (FontAwesomeProSolid f : values()) {
            if (f.getCodepoint() == codepoint) {
                return f;
            }
        }
        throw new IllegalArgumentException("Codepoint " + codepoint + " not found in FontAwesomeProSolid");
    }
